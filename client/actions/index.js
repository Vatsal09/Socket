// TYPES /////////////////////////////////////////////////////////////////////

const LOG_IN = '@@auth/SUBMIT_LOGIN_FORM';
const ADD = '@@test/ADD';

// ACTIONS /////////////////////////////////////////////////////////////////////

function logIn(data) {
  return {
    type: LOG_IN,
    payload: data
  }
}

function add(num) {
  return {
    type: ADD,
    payload: num
  }
}

////////////////////////////////////////////////////////////////////////////////
// EXPORTS /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

module.exports = {
  types: {
    LOG_IN,
    ADD,
  },
  actions: {
    logIn,
    add
  }
}
